public class Es6 {
	public static void main(String[] args) {
		
		int n,m,prod=0;
		Scanner sc = new Scanner(System.in);
		
		System.out.println("Inserisci due numeri interi positivi da moltiplicare tra loro");
		
		n=sc.nextInt();
		m=sc.nextInt();
		
		/*
		int i=0;
		while (i<m) {     metodo while
			prod+=n;
			i++;
		}
		*/
		
		for (int i=0; i<m; i++)  //metodo for
			prod+=n;
		
		System.out.println("Il prodotto tra i due numeri e' " + prod);
		
		sc.close();
	}
}
